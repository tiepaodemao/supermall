<template>
  <div>
    <swiper>
      <swiper-item v-for="item in banners" :key="item.id">
        <a :href="item.link">
          <img :src="item.image" @load="imageLoad"/>
        </a>
      </swiper-item>
    </swiper>
  </div>
</template>

<script>
  import {Swiper,SwiperItem} from 'components/common/swiper/index'
  export default {
    name: "HomeSwiper",
    props: {
      banners: {
        type:Array,
        default(){
          return []
        }
      }
    },
    data(){
      return{
        isLoad: true
      }
    },
    methods: {
      imageLoad(){
        if (this.isLoad){
          console.log('加载首页图片')
          this.$emit("swiperImageLoad")
          this.isLoad = false
        }
      }
    },
    components: {
    Swiper,
    SwiperItem
    }
  }
</script>

